interface Award {
  id: string;
  name: string;
  category: string;
  icon: string;
  description: string;
  awardedAt: string;
  isAutoGenerated: boolean;
  winner: {
    name: string;
    profile: { username: string };
  };
  season: {
    name: string;
    id: string;
  };
}

interface Season {
  id: string;
  name: string;
  isActive: boolean;
}

const iconMap: Record<string, ReactNode> = {
  Crown: <Crown className="h-5 w-5 text-yellow-400" />,
  Trophy: <Trophy className="h-5 w-5 text-amber-400" />,
  Shield: <Shield className="h-5 w-5 text-blue-400" />,
  Star: <Star className="h-5 w-5 text-purple-400" />,
  TrendingUp: <TrendingUp className="h-5 w-5 text-green-400" />,
  Sparkles: <Sparkles className="h-5 w-5 text-pink-400" />,
  Target: <Target className="h-5 w-5 text-red-400" />,
  Award: <Award className="h-5 w-5 text-indigo-400" />,
};

